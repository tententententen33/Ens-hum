class Exp26_3 {
	public static void main(String[] args) {
		int day = 2;
		switch (day) {
			case 1:
				System.out.println("日曜日");
				break;
			case 2:
				System.out.println("月曜日");
				break;
			case 3:
				System.out.println("火曜日");
				break;
			default:
				System.out.println("不明な曜日");
		}
	}
}