class Kadai {
	public static void main(String[] args) {
		double d1 = 3.6;
		double d2 = 1.8;
		double ans;
		ans = d1 + d2;
		System.out.println("3.6+1.8の計算結果:" + ans);
		ans = d1 - d2;
		System.out.println("3.6-1.8の計算結果:" + ans);
		ans = d1 * d2;
		System.out.println("3.6×1.8の計算結果:" + ans);
		ans = d1 / d2;
		System.out.println("3.6÷1.8の計算結果:" + ans);
	}
}