class Exp15_3 {
	public static void main(String[] args) {
		
		int a = 8;
		a -= 2; //aは12になる
		
		int b = 4;
		b *= 3; //bは12になる
		
		int c = 9;
		c /=3; //cは3になる
		
		int d = 10;
		d %= 3; //dは1になる
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}
}