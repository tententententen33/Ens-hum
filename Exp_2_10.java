class Exp_2_10 {
	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 5;
		double num3 = 13;
		double ans;
		ans = num1 + num2;
		System.out.println(ans);
		ans = num1 - num2;
		System.out.println(ans);
		ans = num1 * num2;
		System.out.println(ans);
		ans = num1 / num2;
		System.out.println(ans);
		ans = num3 % num2;
		System.out.println(ans);
		ans = num3 / num2;
		System.out.println(ans);
	}
}