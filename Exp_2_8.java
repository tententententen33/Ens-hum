class Exp_2_8 {
	public static void main(String[] args){
		byte value1 = 0b10;
		short value2 = 0b10010;
		int value3 = 0b11110001001;
		long value4 = 0b100011001010001L;
		byte value8 = 0121;
		int value16_1 = 0x1a;
		int value16_2 = 0xc3b;
		System.out.println(value1);
		System.out.println(value2);
		System.out.println(value3);
		System.out.println(value4);
		System.out.println(value8);
		System.out.println(value16_1);
		System.out.println(value16_2);
	}
}